using System;

class DeclareVariables
{
    static void Main(string[] args)
    {
        ushort ushortNum = 52130;
        Console.WriteLine(ushortNum + " is an ushort type");
        sbyte sbyteNum = -115;
        Console.WriteLine(sbyteNum + " is an sbyte type");
        int intNum = 4825932;
        Console.WriteLine(intNum + " is an int type");
        sbyte sbyteNumber = 97;
        Console.WriteLine(sbyteNumber + " is an sbyte type");
        int intNumber = -10000;
        Console.WriteLine(intNumber + " is an int type");
    }
}

